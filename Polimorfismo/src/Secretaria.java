
public class Secretaria extends Funcionario{
	private double salario;
	private int ramal;
	
	public Secretaria(){
		
	}

	public double getSalario() {
		return salario;
	}

	public void setSalario(double salario) {
		this.salario = salario;
	}

	public int getRamal() {
		return ramal;
	}

	public void setRamal(int ramal) {
		this.ramal = ramal;
	}

	
}
