
public class Loopings {
	public static void main(String[] args){
		int idade = 15;
		while (idade <= 20){
			System.out.println("Enquanto idade for menor que ou igual a 20, idade = " + idade);
			idade = idade + 1;
		}
		System.out.println();
		int i = 0;
		while (i < 10){
			System.out.println("Valor de i = " + i);
            i = i + 1;			
		}
		System.out.println();
		//         for
		for (int ind = 0; ind < 5; ind++){
			System.out.println("for ind = " + ind);
		}
	}

}
